events.onStarted(function()
    events.listen(EntityDeathEvent, function(event)
        local source = event.getDamageSource()
        if source == nil then return end
        local attacker = source.getCausingEntity() ---@type bukkit.Entity?
        if attacker == nil then return end
        if not bukkit.isPlayer(attacker) then return end

        local victim = event.getEntity() ---@type bukkit.Entity
        local victimType = victim.getType().name()

        local d = this.MAP[victimType]
        if d == nil then return end

        local b ---@type bukkit.ItemBuilder?

        local t = type(d)
        if t == "string" then
            b = bukkit.buildItem("PLAYER_HEAD")
                :playerHead_texture(d)
        elseif t == "table" then
            b = bukkit.buildItem(d.material)
        elseif t == "function" then
            b = t(victim)
            if b == nil then return end
        else
            return
        end

        b = this.ITEM(victim, b)
        if b == nil then return end

        bukkit.spawnItem(victim.getLocation(), b:build(), true)
    end)
end)

return this
